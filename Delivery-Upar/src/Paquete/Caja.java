
package Paquete;

public class Caja extends Sobre{
    private float largo;
    private float ancho;
    private float alto;

    public Caja( ) {
    }
    
    public float getLargo() {
        return largo;
    }

    public void setLargo(float largo) {
        this.largo = largo;
    }

    public float getAncho() {
        return ancho;
    }

    public void setAncho(float ancho) {
        this.ancho = ancho;
    }

    public float getAlto() {
        return alto;
    }

    public void setAlto(float alto) {
        this.alto = alto;
    }
    
    
}
